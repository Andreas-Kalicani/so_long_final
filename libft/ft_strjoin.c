#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		string1;
	int		string2;
	char	*result;
	int		i;
	int		j;

	string1 = ft_strlen(s1);
	string2 = ft_strlen(s2);
	result = malloc(sizeof(char) * string1 + string2 + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		result[i + j] = s2[j];
		j++;
	}
	result[i + j] = '\0';
	return (result);
}
